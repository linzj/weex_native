static int embedderDataIndex = 907;
static weex::map_t sMapBundleDigest = weex::hashmap_new();
static weex::map_t sMapDigestCodeCache = weex::hashmap_new();
static weex::map_t sMapDigestIndex = weex::hashmap_new();

v8::Local<v8::Script> V8ScriptRunner::compileAndProduceCodeCache(
    v8::Isolate* isolate, v8::ScriptCompiler::Source* source,
    v8::Local<v8::String> name) {
  v8::Local<v8::Script> script;
  v8::String::Utf8Value nameUtf8(name);

  // long begin = getCPUTime();
  script = v8::ScriptCompiler::Compile(isolate, source,
                                       v8::ScriptCompiler::kProduceCodeCache);
  // long end = getCPUTime();
  // LOGD("Took time %ldms to compile %s and produce revelant code cache",
  //     end - begin, ToCString(nameUtf8));

  return script;
}

v8::Local<v8::Script> V8ScriptRunner::compileAndConsumeCodeCache(
    v8::Isolate* isolate, v8::ScriptCompiler::Source* source,
    v8::Local<v8::String> name) {
  v8::Local<v8::Script> script;
  v8::String::Utf8Value nameUtf8(name);

  // long begin = getCPUTime();
  script = v8::ScriptCompiler::Compile(isolate, source,
                                       v8::ScriptCompiler::kConsumeCodeCache);
  // long end = getCPUTime();
  // LOGD("Took time %ldms to compile %s and consume revelant code cache",
  //	  end - begin, ToCString(nameUtf8));

  return script;
}

v8::Local<v8::Value> V8ScriptRunner::compileAndRunScript(
    v8::Isolate* isolate, v8::Local<v8::Context> context,
    v8::Local<v8::String> code, v8::Local<v8::String> name,
    v8::Local<v8::String> digest, v8::Local<v8::String> codeCachePath) {
  v8::TryCatch try_catch;
  v8::Local<v8::Script> script;
  v8::Local<v8::Value> result;
  v8::String::Utf8Value nameUtf8(name);
  v8::String::Utf8Value digestUtf8(digest);
  v8::String::Utf8Value codeCachePathUtf8(codeCachePath);
  v8::ScriptOrigin origin(name);
  weex::CachedMetaData* cachedMetaData = NULL;
  int* slotCompilingResult = NULL;
  bool ok = true;
  bool isNeedCache = false;

  const char* nameStr = ToCString(nameUtf8);
  const char* digestStr = ToCString(digestUtf8);
  const char* cachePathStr = ToCString(codeCachePathUtf8);
  size_t pathLen = strlen(cachePathStr);
  size_t nameLen = strlen(nameStr);
  size_t digestLen = strlen(digestStr);
  char* valueDigest = NULL;
  char* keyName = new char[nameLen + 1];
  memcpy(keyName, nameStr, nameLen + 1);
  char* keyDigest = new char[digestLen + 1];
  memcpy(keyDigest, digestStr, digestLen + 1);
  char* cacheFilename = new char[pathLen + digestLen + 2];
  memcpy(cacheFilename, cachePathStr, strlen(cachePathStr));
  cacheFilename[pathLen] = '/';
  memcpy(cacheFilename + pathLen + 1, digestStr, digestLen + 1);

  if (weex::hashmap_get(sMapBundleDigest, keyName, (void**)(&valueDigest)) ==
      MAP_MISSING) {
    bool isExisted = false;
    if (access(cacheFilename, F_OK) != -1) {
      isExisted = true;
    }

    mkdir(cachePathStr, 0775);

    // Check if the code cache file exists.
    if (!isExisted) {
      // Now we can compile the script and produce the code cache
      v8::ScriptOrigin origin(name);
      v8::ScriptCompiler::Source source(code, origin);
      v8::Local<v8::Script> script =
          compileAndProduceCodeCache(isolate, &source, name);

      result = script->Run();

      if (result.IsEmpty() || !result->IsFunction()) {
        ok = false;
        LOGE("Failed to compile bundle %s", ToCString(nameUtf8));
      }

      isNeedCache = true;

      if (ok) {
        const v8::ScriptCompiler::CachedData* cachedData =
            source.GetCachedData();
        if (cachedData) {
          cachedMetaData = weex::CachedMetaData::create(
              reinterpret_cast<const char*>(cachedData->data),
              cachedData->length);
        }
        if (cachedMetaData) {
          cachedMetaData->setNeedSerialize(true);
          cachedMetaData->setSerializeFilename(cacheFilename);
          // cachedMetaData->serializeCachedMetaData(cacheFilename);
        }
      }
    } else {
      // The code cache file exists.
      // We can compile the script with the code cache.
      cachedMetaData = weex::CachedMetaData::create(cacheFilename);
      if (cachedMetaData) {
        cachedMetaData->setNeedSerialize(false);

        v8::ScriptCompiler::CachedData* cachedData =
            new v8::ScriptCompiler::CachedData(
                reinterpret_cast<const uint8_t*>(cachedMetaData->data()),
                cachedMetaData->size(),
                v8::ScriptCompiler::CachedData::BufferNotOwned);
        v8::ScriptCompiler::Source source(code, origin, cachedData);
        v8::Local<v8::Script> script =
            compileAndConsumeCodeCache(isolate, &source, name);

        result = script->Run();

        if (result.IsEmpty() || !result->IsFunction()) {
          ok = false;
          LOGE("Failed to compile bundle %s with the code cache.",
               ToCString(nameUtf8));
        }
        isNeedCache = true;
      } else {
        // If failed to use the code cache file,
        // need to delete this file from disk.
        remove(cacheFilename);
      }
    }
  } else {
    if (!strcmp(valueDigest, digestStr)) {
      // If the bundle digest remains the same,
      // just return the cached result.
      if (weex::hashmap_get(sMapDigestIndex, keyDigest,
                            (void**)(&slotCompilingResult)) == MAP_OK) {
        if (slotCompilingResult && *slotCompilingResult != 0) {
          ok = true;
          isNeedCache = false;
          result = context->GetEmbedderData(*slotCompilingResult);
        }
      }
      // If the bundle digest remains the same,
      // just get the cached matadata by the bundle digest.
      else if (weex::hashmap_get(sMapDigestCodeCache, keyDigest,
                                 (void**)(&cachedMetaData)) == MAP_OK) {
        if (cachedMetaData) {
          v8::ScriptCompiler::CachedData* cachedData =
              new v8::ScriptCompiler::CachedData(
                  reinterpret_cast<const uint8_t*>(cachedMetaData->data()),
                  cachedMetaData->size(),
                  v8::ScriptCompiler::CachedData::BufferNotOwned);
          v8::ScriptCompiler::Source source(code, origin, cachedData);
          v8::Local<v8::Script> script =
              compileAndConsumeCodeCache(isolate, &source, name);

          result = script->Run();

          if (result.IsEmpty() || !result->IsFunction()) {
            ok = false;
            LOGE("Failed to compile bundle %s with the code cache.",
                 ToCString(nameUtf8));
          }
        }
      }
    } else {
      // If the bundle digest changed,
      // need to delete the old code cache file, and
      // compile the script and produce the code cache.
      remove(cacheFilename);

      weex::hashmap_remove(sMapBundleDigest, keyName);

      if (weex::hashmap_get(sMapDigestCodeCache, keyDigest,
                            (void**)(&cachedMetaData)) == MAP_OK) {
        delete cachedMetaData;
      }
      weex::hashmap_remove(sMapDigestCodeCache, keyDigest);

      if (weex::hashmap_get(sMapDigestIndex, keyDigest,
                            (void**)(&slotCompilingResult)) == MAP_OK) {
        if (slotCompilingResult) delete slotCompilingResult;
      }
      weex::hashmap_remove(sMapDigestIndex, keyDigest);

      // Now we can compile the script and produce the code cache
      v8::ScriptOrigin origin(name);
      v8::ScriptCompiler::Source source(code, origin);
      v8::Local<v8::Script> script =
          compileAndProduceCodeCache(isolate, &source, name);

      result = script->Run();

      if (result.IsEmpty() || !result->IsFunction()) {
        ok = false;
        LOGE("Failed to compile bundle %s", ToCString(nameUtf8));
      }

      isNeedCache = true;

      if (ok) {
        const v8::ScriptCompiler::CachedData* cachedData =
            source.GetCachedData();
        if (cachedData) {
          cachedMetaData = weex::CachedMetaData::create(
              reinterpret_cast<const char*>(cachedData->data),
              cachedData->length);
        }
        if (cachedMetaData) {
          cachedMetaData->setNeedSerialize(true);
          cachedMetaData->setSerializeFilename(cacheFilename);
          // cachedMetaData->serializeCachedMetaData();
        }
      }
    }
  }

  if (ok && isNeedCache && cachedMetaData) {
    // Map bundle digest and cachedMetaData
    weex::hashmap_put(sMapDigestCodeCache, keyDigest, cachedMetaData);
    // Map bundle url and digest
    weex::hashmap_put(sMapBundleDigest, keyName, keyDigest);
    // Map bundle digest and slot of compiling result
    int* slotResult = new int(embedderDataIndex++);
    weex::hashmap_put(sMapDigestIndex, keyDigest, slotResult);
    context->SetEmbedderData(*slotResult, result);
  } else {
    // sMapDigestCodeCache and sMapBundleDigest hold the referemces
    // of keyName and keyDigest.
    if (keyName) delete[] keyName;
    if (keyDigest) delete[] keyDigest;
  }
  if (cacheFilename) delete[] cacheFilename;

  return result;
}

void V8ScriptRunner::serializeCache() {
  weex::hashmap_iterate(sMapDigestCodeCache, V8ScriptRunner::visit, NULL);
}

int V8ScriptRunner::visit(void* options, void* key, void* data) {
  // Unused options
  (void)(options);
  if (!key || !data) return -1;

  // Serialize the code cache
  CachedMetaData* cache = (CachedMetaData*)data;

  if (cache->needSerialize()) {
    cache->serializeCachedMetaData();
  }

  return 0;
}

